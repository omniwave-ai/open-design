def write_styles(out, title, plan):
    styles_dir=out/'styles'; styles_dir.mkdir(exist_ok=True)
    cards=[]
    for sid,style in STYLES.items():
        (styles_dir/f'{sid}.html').write_text(deck_html(title, plan, sid, style), encoding='utf-8')
        cards.append(f"<a class='card' href='{sid}.html'><b>{style['label']}</b><span>{sid}</span></a>")
    index=f"""<!doctype html><html lang='zh-CN'><head><meta charset='utf-8'><title>Style Exploration</title><style>body{{font-family:-apple-system,BlinkMacSystemFont,'PingFang SC',sans-serif;margin:40px;background:#f6f3ee}}h1{{font-size:48px}}.grid{{display:grid;grid-template-columns:repeat(auto-fit,minmax(260px,1fr));gap:20px}}.card{{display:block;padding:28px;border:1px solid #ddd;border-radius:20px;background:white;color:#111;text-decoration:none}}.card b{{display:block;font-size:24px;margin-bottom:12px}}.card span{{color:#777}}</style></head><body><h1>Humanize PPT 风格探索</h1><p>先用 AST 大纲生成 3 个方向，确定后再进入 presenter / deploy。</p><div class='grid'>{''.join(cards)}</div></body></html>"""
    (styles_dir/'index.html').write_text(index, encoding='utf-8')

def write_presenter(out, title, plan, selected='zara-editorial'):
    pres=out/'presenter'; pres.mkdir(exist_ok=True)
    notes=[{"slide_id":p['slide_id'],"title":p['title'],"note":p['speaker_intent'],"say":p['message']} for p in plan]
    (pres/'notes.json').write_text(json.dumps(notes, ensure_ascii=False, indent=2), encoding='utf-8')
    deck_src=f"../styles/{selected}.html"
    next_src=f"../styles/{selected}.html?slide=1"
    html_doc=f"""<!doctype html><html lang='zh-CN'><head><meta charset='utf-8'><title>{html.escape(title)} · Presenter</title><style>body{{margin:0;background:#0d0d0d;color:#f8f8f8;font-family:-apple-system,BlinkMacSystemFont,'PingFang SC',sans-serif}}.wrap{{display:grid;grid-template-columns:2fr 1fr;height:100vh;gap:16px;padding:16px}}iframe{{width:100%;height:100%;border:0;border-radius:16px;background:white}}.side{{display:grid;grid-template-rows:1fr 1fr 1fr;gap:16px}}.panel{{background:#1b1b1b;border:1px solid #333;border-radius:16px;padding:18px;overflow:auto}}h2{{margin:0 0 12px}}.note{{font-size:22px;line-height:1.45}}button{{font-size:18px;padding:10px 14px;margin-right:8px;border-radius:10px;border:0}}</style></head><body><div class='wrap'><iframe id='current' src='{deck_src}'></iframe><div class='side'><div class='panel'><h2>下一页</h2><iframe id='next' src='{next_src}'></iframe></div><div class='panel'><h2>Speaker Notes</h2><div id='note' class='note'></div></div><div class='panel'><h2>Controls</h2><p id='timer'>00:00</p><button onclick='go(-1)'>上一页</button><button onclick='go(1)'>下一页</button></div></div></div><script>const notes={json.dumps(notes, ensure_ascii=False)};let i=0,t=Date.now();function sync(){{document.getElementById('current').src='{deck_src}?slide='+i;document.getElementById('next').src='{deck_src}?slide='+Math.min(i+1,notes.length-1);document.getElementById('note').innerHTML='<b>'+notes[i].slide_id+' '+notes[i].title+'</b><br>'+notes[i].note+'<hr>'+notes[i].say}}function go(d){{i=Math.max(0,Math.min(notes.length-1,i+d));sync()}}document.addEventListener('keydown',e=>{{if(e.key==='ArrowRight'||e.key===' ')go(1);if(e.key==='ArrowLeft')go(-1)}});setInterval(()=>{{let s=Math.floor((Date.now()-t)/1000);document.getElementById('timer').textContent=String(Math.floor(s/60)).padStart(2,'0')+':'+String(s%60).padStart(2,'0')}},500);sync();</script></body></html>"""
    (pres/'index.html').write_text(html_doc, encoding='utf-8')

def write_deploy(out):
    dep=out/'deploy'; dep.mkdir(exist_ok=True)
    shutil.copytree(out/'styles', dep/'styles', dirs_exist_ok=True)
    shutil.copytree(out/'presenter', dep/'presenter', dirs_exist_ok=True)
    (dep/'index.html').write_text("<meta charset='utf-8'><h1>Humanize PPT Deploy Package</h1><ul><li><a href='styles/index.html'>风格探索</a></li><li><a href='presenter/index.html'>演讲者模式</a></li></ul>", encoding='utf-8')
    (dep/'presenter.html').write_text("<meta http-equiv='refresh' content='0; url=presenter/index.html'>", encoding='utf-8')

def main():
    ap=argparse.ArgumentParser()
    ap.add_argument('--source', required=True)
    ap.add_argument('--out', required=True)
    ap.add_argument('--title', default='Humanize PPT Demo')
    args=ap.parse_args()
    out=Path(args.out)
    text, lines=read_source(args.source)
    plan=make_plan(args.title, lines)
    write_contracts(out, args.title, plan)
    write_styles(out, args.title, plan)
    write_presenter(out, args.title, plan)
    write_deploy(out)
    print(json.dumps({"ok": True, "out": str(out), "style_index": str(out/'styles'/'index.html'), "presenter": str(out/'presenter'/'index.html'), "deploy": str(out/'deploy'/'index.html')}, ensure_ascii=False, indent=2))
if __name__ == '__main__': main()
